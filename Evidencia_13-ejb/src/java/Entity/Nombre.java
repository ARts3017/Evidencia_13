package Entity;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author Arturo Jr
 */
@Entity
@Table(name = "nombre")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Nombre.findAll", query = "SELECT n FROM Nombre n")
    , @NamedQuery(name = "Nombre.findById", query = "SELECT n FROM Nombre n WHERE n.id = :id")
    , @NamedQuery(name = "Nombre.findByNombre", query = "SELECT n FROM Nombre n WHERE n.nombre = :nombre")})
public class Nombre implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Column(name = "nombre")
    private String nombre;

    public Nombre() {
    }

    public Nombre(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Nombre)) {
            return false;
        }
        Nombre other = (Nombre) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Entity.Nombre[ id=" + id + " ]";
    }
    
}
